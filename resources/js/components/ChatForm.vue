<template>
    <input class="input" type="text" placeholder="Type your message here..." v-model="newMessage" @keyup.enter="sendMessage" autofocus>
</template>

<script>
    export default {
        props: ['user'],

        data() {
            return {
                newMessage: ''
            }
        },

        methods: {
            sendMessage() {
                this.$emit('messagesent', {
                    user: this.user,
                    message: this.newMessage
                });

                this.newMessage = ''
            }
        }    
    }
</script>
